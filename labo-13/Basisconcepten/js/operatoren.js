// Voorbeeldwaarden
let a = 10;
let b = 3;

// Optelling (+)
let optelling = a + b;
console.log("Optelling (a + b):", optelling); // Output: 13

// Aftrekking (-)
let aftrekking = a - b;
console.log("Aftrekking (a - b):", aftrekking); // Output: 7

// Vermenigvuldiging (*)
let vermenigvuldiging = a * b;
console.log("Vermenigvuldiging (a * b):", vermenigvuldiging); // Output: 30

// Deling (/)
let deling = a / b;
console.log("Deling (a / b):", deling); // Output: 3.333...

// Rest (modulo) (%)
let rest = a % b;
console.log("Rest (a % b):", rest); // Output: 1

// Exponent (**)
let exponent = a ** b;
console.log("Exponent (a ** b):", exponent); // Output: 1000



// VERKORTE NOTATIE:

let i = 10; // Startwaarde

// Verhogen met 1 (i++)
i++; // is hetzelfde als i = i + 1;
console.log("i++:", i); // Output: 11

// Verlagen met 1 (i--)
i--; // is hetzelfde als i = i - 1;
console.log("i--:", i); // Output: 10

// Verhogen met een getal (i += 3)
i += 3; // is hetzelfde als i = i + 3;
console.log("i += 3:", i); // Output: 13

// Verminderen met een getal (i -= 6)
i -= 6; // is hetzelfde als i = i - 6;
console.log("i -= 6:", i); // Output: 7

// Vermenigvuldigen met een getal (i *= 7)
i *= 7; // is hetzelfde als i = i * 7;
console.log("i *= 7:", i); // Output: 49

// Delen door een getal (i /= 2)
i /= 2; // is hetzelfde als i = i / 2;
console.log("i /= 2:", i); // Output: 24.5